// 52.	Write a program to create a Simple Calculator using a switch case. 
// Define numbers and operator
let num1 = 10;
let num2 = 5;
let operator = '/';



switch (operator) {
    case '+':
         console.log(num1 + num2);
        break;
    case '-':
         console.log(num1 - num2);
        break;
    case '*':
         console.log(num1 * num2);
        break;
    case '/':
         console.log(num1 / num2);
        break;
    default:
         console.log("Invalid");
}




